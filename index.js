let language = 'ru';
let register = 'lower';
let isCapsLock = false;

const keys = document.querySelectorAll('.key');

const redraw = () => {
  for (let i = 0; i < keys.length; i += 1) {
    const key = keys[i];

    const ruKeys = ['Backquote', 'BracketLeft', 'BracketRight', 'Semicolon', 'Quote', 'Comma', 'Period'];

    let keyRegister = register;
    const keyId = key.id.toString();

    if (isCapsLock && (keyId.includes('Key') || (language === 'ru' && (ruKeys.some((k) => k === keyId))))) {
      keyRegister = register === 'upper' ? 'upper' : 'lower';
    } else if (isCapsLock) {
      keyRegister = register === 'upper' ? 'lower' : 'upper';
    }

    key.innerHTML = keyboard[language][keyRegister][key.id];
  }
};
redraw();

const switchRegister = (e) => {
  if (e.code === 'ShiftRight' || e.code === 'ShiftLeft') {
    register = register === 'upper' ? 'lower' : 'upper';
    redraw();
  }
};

const switchLanguage = (e) => {
  if ((e.code === 'ShiftLeft' && e.altKey)
    || (e.code === 'ShiftRight' && e.altKey)) {
    language = language === 'ru' ? 'en' : 'ru';
  }
};

const toggleCapsLock = (e) => {
  if (e.code === 'CapsLock') {
    const capsLockKey = document.getElementById(e.code);

    register = isCapsLock ? 'lower' : 'upper';

    if (!isCapsLock) {
      capsLockKey.classList.add('active');
      isCapsLock = true;
    } else {
      capsLockKey.classList.remove('active');
      isCapsLock = false;
    }

    redraw();
  }
};

document.addEventListener('keydown', (e) => {
  const activeKey = document.getElementById(e.code);

  if (e.code === 'Tab') {
    e.preventDefault();
  }
  switchRegister(e);
  toggleCapsLock(e);
  switchLanguage(e);

  if (e.code === 'CapsLock') {
    return;
  }
  if (activeKey) {
    activeKey.classList.add('active');
  }
});

document.addEventListener('keyup', (e) => {
  const activeKey = document.getElementById(e.code);
  switchRegister(e);

  if (e.code === 'CapsLock') {
    return;
  }
  if (activeKey) {
    activeKey.classList.remove('active');
  }
});
